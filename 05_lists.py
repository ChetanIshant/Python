#list is a mutable data type in python
#it is started and ended with square brackets []
my_list = [1, 2, 3, 4, 5]
print(my_list)
#list can have different data types
my_list2 = [1, "hello", 3.5, True]
print(my_list2)
my_list2.append("new item") #to add an item at the end of the list  

print(my_list2)

#Lists in Python are ordered and mutable data structures used to store multiple items in a single variable. 
#They are defined using square brackets [] and can contain elements of different data types, such as integers, strings, floats, or even other lists. 
#Lists support indexing, slicing, and many built-in methods like append(), remove(), and sort(). Because they are mutable, 
#their elements can be modified after creation, making them highly flexible.
